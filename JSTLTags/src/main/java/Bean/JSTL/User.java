package Bean.JSTL;

public class User {
	String firstName;
	String lastName;
	
	public User() {
		firstName="Naveen";
		lastName="Kumar";
		
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getSecondName() {
		return lastName;
	}

	public void setSecondName(String lastName) {
		this.lastName = lastName;
	}
	
}
